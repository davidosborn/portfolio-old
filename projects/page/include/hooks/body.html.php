<h1>Portable Adventure Game Engine</h1>

<p>
	This is an engine for 3D point-and-click adventure games.
	I have been working on it in my spare time since 2006.
	It is programmed in C++11, OpenGL, Boost, Lua, and a number of other open-source libraries.
</p>

<p>
	You can download the latest demo for <a href="<?php echo PAGE_URL?>include/assets/page-demo.zip">Windows</a> and <strike>Linux</strike> (not available right now).
	The <a href="https://github.com/davidcosborn/page/">source code</a> is available on GitHub under an open-source license.
	I also maintain a <a href="http://www.gamedev.net/blog/331-journal-of-dcosborn/">journal</a> for the project on gamedev.net.
</p>

