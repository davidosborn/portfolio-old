<h1>Features</h1>
<ul>
	<li>Runs on Windows and Linux.</li>
	<li>Programmed to the latest C++ standard (2011).</li>
	<li>Uses a configurable build system, with many optional dependencies to enable specific features.</li>
	<li>Comes with a suite of Python scripts for producing content in <a href="http://www.blender.org/">Blender</a>.</li>
	<li>Programmable animation controllers and weighted skeletal-animation.</li>
	<li>Constant-framerate video recording.</li>
	<li>Arbitrary-resolution screenshots [<a href="http://www.gameenginegems.net/gemsdb/article.php?id=178">Vlachos 2001</a>].</li>
	<li>Variance shadow maps [<a href="http://www.punkuser.net/vsm/">Donnelly 2006</a>].</li>
	<li>GPU median-filtering [<a href="http://graphics.cs.williams.edu/papers/MedianShaderX6/">McGuire 2008</a>].</li>
	<li>Scriptable with Lua.</li>
	<li>Streaming audio.</li>
</ul>

