<h1>Travel Experts</h1>

<h2>First Prototype</h2>
<p>
	I collaborated with Frank (Xing) Cai, Robyn Hernandez, and Silviu Bobea on this website for a hypothetical travel agency.
	It was our first group project in the OOSD program at SAIT.
	We developed the website using PHP, MySQL, HTML, CSS, and Javascript.
</p>
<h3>What I did</h3>
<ul>
	<li>Managed the source control using Git, BitBucket.org, and Google Drive.</li>
	<li>Merged the code from my teammates into the origin/master repository.</li>
	<li>Created an online administration area (with HTML5 content editing) for use by logged-in agents.</li>
	<li>Wrote some foundational PHP code allowing for seamless relocation of the website.</li>
	<li>Created the weather button (which used a service that may no longer be available).</li>
	<li>Created the "About Us" page.</li>
	<li>Created the content boxes on the main page.</li>
</ul>
