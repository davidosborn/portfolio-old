<h1>Explorer</h1>
<p>
	A realtime 3D visualization by David Osborn and Helen He for the
	<em>Art Science and Technology</em> course, a collaboration between the
	<a href="http://www.acad.ca/">Alberta College of Art and Design</a> and the
	<a href="http://www.ucalgary.ca/">University of Calgary</a>.
</p>
<p>
	I did the majority of the programming, using C++ and OpenGL, and some of the realistic textures, while Helen created a lot of the non-photorealistic content and a bunch of the documentation.
</p>
<p>
	Check out the
	<a href="http://innovis.cpsc.ucalgary.ca/Research/Explorer">project page</a>
	on the University of Calgary's Innovis site.
</p>

